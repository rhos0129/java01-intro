package variable;

public class Var2 {

    public static void main(String[] args) {

        // 변수 선언
        // 숫자 정수(integer)를 보관할 수 있는 이름이 a 라는 데이터 저장소 생성
        int a;

        // 변수에 값 대입
        // 자바에서 = 은 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻 (수학에서의 =와는 다름)
        // 변수 초기화 : 선언한 변수에 처음으로 값을 대입해서 저장하는 것
        a = 20; //10 -> 20

        // 변수 값 읽기
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
    }

}
